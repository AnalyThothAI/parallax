def _sqlalchemy_url(settings: Settings | None) -> str:
    test_dsn = os.environ.get(TEST_POSTGRES_DSN_ENV, "").strip()
    if test_dsn:
        dsn = test_dsn
    else:
        if settings is None:
            settings = load_settings(require_ws_token=False)
        dsn = local_docker_host_dsn(
            with_password_from_file(settings.storage.postgres.dsn, settings.postgres_password_file)
        )
    if dsn.startswith("postgresql://"):
        return "postgresql+psycopg://" + dsn.removeprefix("postgresql://")
    return dsn


def _require_database_at_alembic_head(connection: Connection) -> None:
    expected = latest_migration_version()
    versions = [
        str(version)
        for version in connection.exec_driver_sql("SELECT version_num FROM alembic_version ORDER BY version_num")
        .scalars()
        .all()
    ]
    if versions != [expected]:
        actual = ",".join(versions) if versions else "<missing>"
        raise RuntimeError(f"db_schema_generation_requires_alembic_head: expected={expected} actual={actual}")


def _require_retired_tables_absent(table_names: Sequence[str]) -> None:
    retired_tables_present = sorted(RETIRED_TABLES_AT_HEAD.intersection(table_names))
    if retired_tables_present:
        raise RuntimeError(
            "db_schema_generation_requires_hard_cut_schema: retired_tables_present=" + ",".join(retired_tables_present)
        )


def main() -> None:
    settings = None if os.environ.get(TEST_POSTGRES_DSN_ENV, "").strip() else load_settings(require_ws_token=False)
    engine = create_engine(_sqlalchemy_url(settings), pool_pre_ping=True)
    try:
        with engine.connect() as connection:
            _require_database_at_alembic_head(connection)
            inspector = inspect(connection)
            table_names = sorted(inspector.get_table_names(schema="public"))
            _require_retired_tables_absent(table_names)
            body = ["# Database Schema", ""]
            for table in table_names:
                body.append(f"## `{table}`")
                body.append("")
                body.append("| Column | Type | Nullable | Default |")
                body.append("|--------|------|----------|---------|")
                body.extend(
                    f"| `{col['name']}` | `{col['type']}` | {col.get('nullable', True)} | `{col.get('default')}` |"
                    for col in inspector.get_columns(table, schema="public")
                )
                body.append("")
        while body and body[-1] == "":
            body.pop()
        OUTPUT.write_text(HEADER + "\n".join(body) + "\n", encoding="utf-8")
    finally:
        engine.dispose()


if __name__ == "__main__":
    main()
