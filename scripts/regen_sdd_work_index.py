from __future__ import annotations

import argparse
import re
import sys
from collections import Counter
from dataclasses import dataclass
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
SDD_FEATURES = ROOT / "docs" / "sdd" / "features"
OUTPUT = ROOT / "docs" / "generated" / "sdd-work-index.md"

ARTIFACTS = ("spec.md", "plan.md", "tasks.md", "verification.md")
ACTIVE_STATUSES = {"draft", "approved", "in progress", "review", "blocked"}
COMPLETED_STATUSES = {"verified", "superseded"}
STATUS_RE = re.compile(r"^\s*(?:\*\*)?Status(?:\*\*)?\s*:\s*(.+?)\s*$", re.IGNORECASE)


@dataclass(frozen=True)
class SddArtifact:
    feature: str
    state: str
    artifact: str
    path: Path
    status: str
    flags: tuple[str, ...]

    @property
    def relative_path(self) -> str:
        return self.path.relative_to(ROOT).as_posix()


def main() -> int:
    parser = argparse.ArgumentParser(description="Regenerate docs/generated/sdd-work-index.md")
    parser.add_argument("--check", action="store_true", help="exit non-zero if the generated file is stale")
    args = parser.parse_args()

    text = render_index(scan_sdd_artifacts())
    if args.check:
        existing = OUTPUT.read_text(encoding="utf-8") if OUTPUT.exists() else ""
        if existing != text:
            print(
                "docs/generated/sdd-work-index.md is stale; run `uv run python scripts/regen_sdd_work_index.py`.",
                file=sys.stderr,
            )
            return 1
        return 0

    OUTPUT.write_text(text, encoding="utf-8")
    print(f"wrote {OUTPUT.relative_to(ROOT).as_posix()}")
    return 0


def scan_sdd_artifacts() -> list[SddArtifact]:
    artifacts: list[SddArtifact] = []
    for state in ("active", "completed"):
        lane_root = SDD_FEATURES / state
        if not lane_root.exists():
            continue
        feature_dirs = sorted(path for path in lane_root.iterdir() if path.is_dir() and not path.name.startswith("."))
        for feature_dir in feature_dirs:
            for artifact_name in ARTIFACTS:
                path = feature_dir / artifact_name
                status = extract_status(path) if path.exists() else "missing-file"
                artifacts.append(
                    SddArtifact(
                        feature=feature_dir.name,
                        state=state,
                        artifact=artifact_name,
                        path=path,
                        status=status,
                        flags=lifecycle_flags(state=state, status=status),
                    )
                )
    return artifacts


def extract_status(path: Path) -> str:
    for line in path.read_text(encoding="utf-8").splitlines()[:40]:
        match = STATUS_RE.match(line)
        if match:
            return normalize_cell(match.group(1))
    return "unspecified"


def lifecycle_flags(*, state: str, status: str) -> tuple[str, ...]:
    normalized = status.lower()
    flags: list[str] = []
    if normalized in {"unspecified", "missing-file"}:
        flags.append("missing-status")
    if state == "active" and normalized not in ACTIVE_STATUSES and "missing-status" not in flags:
        flags.append("review-lifecycle")
    if state == "completed" and normalized not in COMPLETED_STATUSES and "missing-status" not in flags:
        flags.append("review-lifecycle")
    return tuple(flags)


def render_index(items: list[SddArtifact]) -> str:
    feature_counts = Counter(item.state for item in _unique_features(items))
    artifact_counts = Counter(item.state for item in items)
    flag_counts = Counter(flag for item in items for flag in item.flags)

    lines = [
        "# SDD Work Index",
        "",
        "> Generated by `scripts/regen_sdd_work_index.py`. Do not hand-edit.",
        "",
        (
            "This index makes the current SDD lane visible to coding agents. "
            "Feature artifacts are execution records; canonical project truth remains in code, "
            "root governance docs, domain architecture maps, tests, and generated contracts."
        ),
        "",
        "## Summary",
        "",
        "| State | Features | Artifacts |",
        "|-------|----------|-----------|",
        f"| `active` | {feature_counts['active']} | {artifact_counts['active']} |",
        f"| `completed` | {feature_counts['completed']} | {artifact_counts['completed']} |",
        "",
        "## Lifecycle Flags",
        "",
        "| Flag | Count | Meaning |",
        "|------|-------|---------|",
        (
            "| `review-lifecycle` | "
            f"{flag_counts['review-lifecycle']} | Artifact status is not valid for its lane. "
            "Active artifacts must stay in an active status; completed artifacts must be "
            "`Verified` or `Superseded`. |"
        ),
        (
            "| `missing-status` | "
            f"{flag_counts['missing-status']} | Artifact is missing or has no status line in its first 40 lines. |"
        ),
        "",
        "## Artifacts",
        "",
        "| Feature | State | Artifact | Status | Flags | Path |",
        "|---------|-------|----------|--------|-------|------|",
    ]
    for item in items:
        flags = ", ".join(f"`{flag}`" for flag in item.flags) or "-"
        lines.append(
            f"| `{item.feature}` | `{item.state}` | `{item.artifact}` | "
            f"{markdown_escape(item.status)} | {flags} | `{item.relative_path}` |"
        )
    lines.append("")
    return "\n".join(lines)


def _unique_features(items: list[SddArtifact]) -> list[SddArtifact]:
    seen: set[tuple[str, str]] = set()
    unique: list[SddArtifact] = []
    for item in items:
        key = (item.state, item.feature)
        if key in seen:
            continue
        seen.add(key)
        unique.append(item)
    return unique


def normalize_cell(value: str) -> str:
    normalized = " ".join(str(value).strip().split())
    return normalized.replace("|", "\\|") or "unspecified"


def markdown_escape(value: str) -> str:
    return value.replace("|", "\\|")


if __name__ == "__main__":
    raise SystemExit(main())
