# tests/contract/test_openapi_drift.py
"""Contract test: regenerate OpenAPI + frontend types and assert no drift.

If this test fails, run `make regen-contract` to update the committed artefacts,
then commit the resulting diff so reviewers can see the schema change.

Note on types layout
--------------------
web/src/lib/types/openapi.ts  — auto-generated by openapi-typescript; only
                                OpenAPI path/component/operation interfaces live here.
web/src/api/types.ts          — temporary hand-authored domain types until the
                                frontend facade moves fully to generated contracts.
"""

from __future__ import annotations

import json
import subprocess
from pathlib import Path

import pytest

ROOT = Path(__file__).resolve().parents[2]
OPENAPI_PATH = ROOT / "docs" / "generated" / "openapi.json"
OPENAPI_TS_PATH = ROOT / "web" / "src" / "lib" / "types" / "openapi.ts"


@pytest.mark.contract
def test_openapi_json_matches_committed_artefact(tmp_path: Path) -> None:
    """Regenerate openapi.json into a tmp dir and compare bytes with the committed one."""
    from gmgn_twitter_intel.app.runtime.app import create_app
    from gmgn_twitter_intel.platform.config.settings import Settings

    settings = Settings(ws_token="schema-gen-placeholder")
    app = create_app(settings=settings, start_collector=False)
    fresh = json.dumps(app.openapi(), indent=2, sort_keys=True) + "\n"
    committed = OPENAPI_PATH.read_text(encoding="utf-8")
    if fresh != committed:
        diff_path = tmp_path / "openapi.fresh.json"
        diff_path.write_text(fresh, encoding="utf-8")
        pytest.fail(
            "OpenAPI schema drifted from docs/generated/openapi.json.\n"
            f"Run `make regen-contract` to update the committed artefacts.\n"
            f"Fresh schema written to {diff_path} for inspection."
        )


@pytest.mark.contract
def test_openapi_ts_matches_committed_artefact(tmp_path: Path) -> None:
    """Regenerate web/src/lib/types/openapi.ts and compare with the committed one."""
    fresh_path = tmp_path / "openapi.ts"
    result = subprocess.run(
        [
            "npx",
            "openapi-typescript",
            str(OPENAPI_PATH),
            "-o",
            str(fresh_path),
        ],
        cwd=str(ROOT / "web"),
        capture_output=True,
        text=True,
        check=False,
    )
    if result.returncode != 0:
        pytest.fail(
            "openapi-typescript invocation failed.\n"
            f"stdout: {result.stdout}\n"
            f"stderr: {result.stderr}\n"
            "Make sure `cd web && npm install` has run."
        )
    fresh = fresh_path.read_text(encoding="utf-8")
    committed = OPENAPI_TS_PATH.read_text(encoding="utf-8")
    if fresh != committed:
        pytest.fail(
            f"Frontend OpenAPI types drifted from {OPENAPI_TS_PATH.relative_to(ROOT)}.\n"
            "Run `make regen-contract` to update the committed artefacts."
        )


@pytest.mark.contract
def test_signal_pulse_item_schema_matches_runtime_payload_keys() -> None:
    """The OpenAPI item contract should expose the fields emitted by SignalPulseService."""
    schema = json.loads(OPENAPI_PATH.read_text(encoding="utf-8"))
    props = schema["components"]["schemas"]["SignalPulseItem"]["properties"]

    expected = {
        "candidate_id",
        "candidate_type",
        "subject_key",
        "subject",
        "target_type",
        "target_id",
        "symbol",
        "window",
        "scope",
        "pulse_status",
        "verdict",
        "social_phase",
        "candidate_score",
        "score_band",
        "gate_reasons",
        "risk_reasons",
        "last_edge_events",
        "evidence_event_ids",
        "source_event_ids",
        "factor_snapshot",
        "decision",
        "gate",
        "fact_card",
        "agent_run_id",
        "pulse_version",
        "gate_version",
        "prompt_version",
        "schema_version",
        "created_at_ms",
        "updated_at_ms",
        "playbooks",
        "stages",
    }
    assert expected <= set(props)
    assert "status" not in props
    assert "target" not in props

    decision = schema["components"]["schemas"]["SignalPulseDecision"]
    assert {
        "route",
        "recommendation",
        "confidence",
        "summary_zh",
        "abstain_reason",
        "narrative_archetype",
        "narrative_thesis_zh",
        "bull_view",
        "bear_view",
        "playbook",
        "evidence_event_ids",
        "evidence_event_urls",
        "invalidation_conditions",
        "residual_risks",
    } <= set(decision["required"])
