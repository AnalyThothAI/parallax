const hasChinese = (value: string) => /[\u3400-\u9fff]/u.test(value);

describe("macroDisplay current vocabulary", () => {
  it("covers exactly the 108 manifest concepts and seven current derived concepts", () => {
    expect(Object.keys(MACRO_CONCEPT_LABELS).sort()).toEqual([...CURRENT_CONCEPT_KEYS].sort());
    for (const [key, label] of Object.entries(MACRO_CONCEPT_LABELS)) {
      expect(label).not.toBe(key);
      expect(hasChinese(label)).toBe(true);
    }
  });

  it("covers exactly the current judgment, stage, rule, evidence and exposure codes", () => {
    expect(Object.keys(MACRO_CODE_LABELS).sort()).toEqual([...CURRENT_CODE_KEYS].sort());
    for (const [key, label] of Object.entries(MACRO_CODE_LABELS)) {
      expect(label).not.toBe(key);
      expect(hasChinese(label)).toBe(true);
    }
  });

  it("covers current named capabilities and exact reason codes without retired aliases", () => {
    expect(Object.keys(MACRO_CAPABILITY_LABELS).sort()).toEqual([...CURRENT_CAPABILITIES].sort());
    expect(Object.keys(MACRO_REASON_LABELS).sort()).toEqual([...CURRENT_EXACT_REASONS].sort());
    expect(MACRO_CONCEPT_LABELS).not.toHaveProperty("catalyst:fomc");
    expect(MACRO_CONCEPT_LABELS).not.toHaveProperty("credit:hy_effective_yield");
    expect(MACRO_CONCEPT_LABELS).not.toHaveProperty("credit:sloos");
    expect(MACRO_CONCEPT_LABELS).not.toHaveProperty("derived:sofr_minus_iorb");
    expect(MACRO_CODE_LABELS).not.toHaveProperty("bear_steepening");
    expect(MACRO_CODE_LABELS).not.toHaveProperty("inside_corridor");
  });

  it("translates current dynamic capability, gap and reason families", () => {
    expect(macroCapabilityLabel("official_catalyst:event:fomc_decision_next")).toBe(
      "官方催化：下一次 FOMC 利率决议",
    );
    expect(macroGapLabel("cross_asset_return_60:asset:spy")).toBe(
      "60 个交易日收益缺口：标普 500 ETF（SPY）",
    );
    expect(macroGapLabel("cross_asset_correlation_60:asset:spy:asset:hyg")).toContain(
      "标普 500 ETF（SPY） × 高收益公司债 ETF（HYG）",
    );
    expect(macroReasonLabel("insufficient_history:20_sessions")).toBe("历史样本不足：20 个交易日");
    expect(macroReasonLabel("unit_mismatch:percent")).toBe("计量单位与合同不一致：应为百分比");
    expect(macroReasonLabel("frequency_mismatch:weekly")).toBe("数据频率与合同不一致：应为周频");
    expect(
      macroReasonLabel(
        "missing_valid_observation_metadata_and_event_time_and_timezone_and_source_url",
      ),
    ).toBe("官方事件元数据不完整");
  });

  it("fails closed to Chinese placeholders while preserving raw refs separately", () => {
    expect(macroLabel("new_machine_state")).toBe("未识别标签");
    expect(macroCodeLabel("new_rule")).toBe("未命名规则或状态");
    expect(macroConceptLabel("provider:new_series")).toBe("未命名宏观证据");
    expect(macroCapabilityLabel("new_capability")).toBe("未命名未评估能力");
    expect(macroReasonLabel("new_reason")).toBe("未识别原因");
    expect(macroGapLabel("new_gap")).toBe("未识别证据缺口");
    expect(macroUnitLabel("new_unit")).toBe("未识别单位");
    expect(macroWindowLabel("new_window")).toBe("未识别窗口");
    expect(macroEvidenceRefsLabel(["rates:dgs10"])).toBe("美国 10 年期国债收益率（rates:dgs10）");
  });
});
