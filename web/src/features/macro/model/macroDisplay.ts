export function macroLabel(value: string | null | undefined): string {
  if (!value) return "未提供";
  return MACRO_CODE_LABELS[value as keyof typeof MACRO_CODE_LABELS] ?? "未识别标签";
}

export function macroConceptLabel(value: string): string {
  return MACRO_CONCEPT_LABELS[value as keyof typeof MACRO_CONCEPT_LABELS] ?? "未命名宏观证据";
}

export function macroCodeLabel(code: string): string {
  return MACRO_CODE_LABELS[code as keyof typeof MACRO_CODE_LABELS] ?? "未命名规则或状态";
}

export function macroCapabilityLabel(capability: string): string {
  const exact = MACRO_CAPABILITY_LABELS[capability as keyof typeof MACRO_CAPABILITY_LABELS];
  if (exact) return exact;
  const catalystConcept = capability.startsWith("official_catalyst:")
    ? capability.slice("official_catalyst:".length)
    : null;
  return catalystConcept ? `官方催化：${macroConceptLabel(catalystConcept)}` : "未命名未评估能力";
}

export function macroReasonLabel(reason: string): string {
  const exact = MACRO_REASON_LABELS[reason as keyof typeof MACRO_REASON_LABELS];
  if (exact) return exact;
  const [prefix, detail] = reason.split(":", 2);
  if (prefix === "insufficient_history" && detail) {
    return `历史样本不足：${macroWindowLabel(detail)}`;
  }
  if (prefix === "unit_mismatch" && detail) {
    return `计量单位与合同不一致：应为${macroUnitLabel(detail)}`;
  }
  if (prefix === "frequency_mismatch" && detail) {
    return `数据频率与合同不一致：应为${macroLabel(detail)}`;
  }
  if (prefix === "data_quality" && detail) {
    return `数据质量未通过：${detail}`;
  }
  if (reason.startsWith("missing_") && reason.includes("_and_")) {
    return "官方事件元数据不完整";
  }
  return "未识别原因";
}

export function macroGapLabel(value: string): string {
  if (value in MACRO_CONCEPT_LABELS) return macroConceptLabel(value);
  if (value in MACRO_CAPABILITY_LABELS || value.startsWith("official_catalyst:")) {
    return macroCapabilityLabel(value);
  }
  const returnPrefix = "cross_asset_return_60:";
  if (value.startsWith(returnPrefix)) {
    return `60 个交易日收益缺口：${macroConceptLabel(value.slice(returnPrefix.length))}`;
  }
  const correlationPrefix = "cross_asset_correlation_60:";
  if (value.startsWith(correlationPrefix)) {
    const parts = value.slice(correlationPrefix.length).split(":");
    if (parts.length === 4) {
      const left = `${parts[0]}:${parts[1]}`;
      const right = `${parts[2]}:${parts[3]}`;
      return `60 个交易日相关性缺口：${macroConceptLabel(left)} × ${macroConceptLabel(right)}`;
    }
  }
  return "未识别证据缺口";
}

export function macroEvidenceRefsLabel(values: readonly string[]): string {
  if (!values.length) return "无";
  return values.map((value) => `${macroConceptLabel(value)}（${value}）`).join(" · ");
}

export function macroUnitLabel(value: string | null | undefined): string {
  if (!value) return "单位未提供";
  return UNIT_LABELS[value] ?? "未识别单位";
}

export function macroWindowLabel(value: string | null | undefined): string {
  if (!value) return "窗口未提供";
  return WINDOW_LABELS[value] ?? "未识别窗口";
}

export function formatMacroNumber(value: number | null): string {
  if (value === null || !Number.isFinite(value)) return "未提供";
  return new Intl.NumberFormat("zh-CN", {
    maximumFractionDigits: 4,
    minimumFractionDigits: 0,
  }).format(value);
}

export function formatMacroTimestamp(value: number): string {
  return new Date(value).toISOString();
}

export function formatMacroSample(sample: {
  count: number;
  end: string | null;
  start: string | null;
}): string {
  const range = sample.start && sample.end ? `${sample.start} → ${sample.end}` : "区间未提供";
  return `${range} · ${sample.count} 个样本`;
}
